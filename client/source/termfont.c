// ANSI base palette (indices 0-7) + the renderer defaults (index 9). ABGR8888
// (0xAABBGGRR) as citro2d expects. Bold brightens the fg toward the bright set.
static const u32 s_fg[10] = {
  0xFF2A2A2A, // 0 black (kept off pure-black so it reads on the bg)
  0xFF4C4CE0, // 1 red
  0xFF4CC24C, // 2 green
  0xFF41B3E0, // 3 yellow (amber)
  0xFFE08A41, // 4 blue
  0xFFB341E0, // 5 magenta
  0xFFC2A24C, // 6 cyan
  0xFFF0F0F0, // 7 white
  0xFFF0F0F0, // 8 (unused)
  0xFFF0F0F0, // 9 default fg
};
static const u32 s_fg_bold[10] = {
  0xFF808080, 0xFF6060FF, 0xFF6CE26C, 0xFF61D3FF, 0xFFFFAA61,
  0xFFD361FF, 0xFFE2C26C, 0xFFFFFFFF, 0xFFFFFFFF, 0xFFFFFFFF,
};
static const u32 s_bg[10] = {
  0xFF1E1E1E, // 0 black
  0xFF20209A, // 1 red
  0xFF209A20, // 2 green
  0xFF2080B0, // 3 yellow
  0xFF9A5020, // 4 blue
  0xFF80209A, // 5 magenta
  0xFF807020, // 6 cyan
  0xFFC0C0C0, // 7 white
  0xFF1E1E1E, // 8 (unused)
  0xFF1E1E1E, // 9 default bg
};

static void draw_glyph(char ch, u32 color, float x, float y) {
  if (ch < 0x20 || ch > 0x7e) return;
  const uint8_t *g = s_font[(int)ch - 0x20];
  for (int row = 0; row < AB_TERMFONT_GLYPH_H; row++) {
    uint8_t bits = g[row];
    if (!bits) continue;
    for (int col = 0; col < AB_TERMFONT_GLYPH_W; col++) {
      if (bits & (1u << col)) {
        C2D_DrawRectSolid(x + (float)col, y + (float)row, 0.0f, 1.0f, 1.0f, color);
      }
    }
  }
}

void ab_termfont_draw(const ab_term *t) {
  int cur_r = ab_term_cursor_row(t);
  int cur_c = ab_term_cursor_col(t);
  bool at_bottom = ab_term_scroll_offset(t) == 0;

  for (int r = 0; r < AB_TERM_ROWS; r++) {
    float cy = AB_TERMFONT_ORIGIN_Y + (float)r * AB_TERMFONT_CELL_H;
    for (int c = 0; c < AB_TERM_COLS; c++) {
      float cx = AB_TERMFONT_ORIGIN_X + (float)c * AB_TERMFONT_CELL_W;
      ab_cell cell = ab_term_cell(t, r, c);

      int fg_idx = cell.attr & AB_ATTR_FG_MASK;
      int bg_idx = (cell.attr & AB_ATTR_BG_MASK) >> AB_ATTR_BG_SHIFT;
      bool bold = (cell.attr & AB_ATTR_BOLD) != 0;
      bool inverse = (cell.attr & AB_ATTR_INVERSE) != 0;
      bool cursor_here = at_bottom && r == cur_r && c == cur_c;
      if (cursor_here) inverse = !inverse;

      u32 fg = (bold ? s_fg_bold : s_fg)[fg_idx];
      u32 bg = s_bg[bg_idx];
      if (inverse) {
        u32 tmp = fg;
        fg = bg;
        bg = tmp;
      }

      // Cell background (only when it differs from the default, to save draws —
      // the top target is already cleared to the default bg).
      if (bg_idx != AB_ATTR_DEFAULT_COLOR || inverse) {
        C2D_DrawRectSolid(cx, cy, 0.0f, (float)AB_TERMFONT_CELL_W,
                          (float)AB_TERMFONT_CELL_H, bg);
      }
      if (cell.ch != ' ') draw_glyph(cell.ch, fg, cx, cy);
    }
  }
}
